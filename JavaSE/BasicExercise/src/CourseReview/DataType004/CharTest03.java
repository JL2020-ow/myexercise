package CourseReview.DataType004;

public class CharTest03 {
	public static void main(String[] args) {
		char c1 = 'a';
		System.out.println(c1);
		
		char c2 = 97;
		System.out.println(c2);
		
		//char类型的取值范围[0~65535]
		char c3 = 65535;
		System.out.println(c3);
		char c4 =(char)65535;
		System.out.println(c4);
	}
}
